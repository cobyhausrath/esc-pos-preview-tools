/**
 * Example 2: Simple Receipt
 * Demonstrates: A realistic receipt with header, items, and total
 */

export const pythonCode = `from escpos.printer import Usb

# Initialize printer
p = Usb(0x04b8, 0x0e15)

# Header
p.set(align='center', bold=True, width=2, height=2)
p.text("ACME STORE\\n")
p.set(bold=False, width=1, height=1)
p.text("123 Main Street\\n")
p.text("Phone: 555-0123\\n")
p.text("\\n")

# Receipt details
p.set(align='left')
p.text("Receipt #12345\\n")
p.text("Date: 2025-01-15\\n")
p.text("--------------------------------\\n")

# Items
p.text("Coffee             $3.50\\n")
p.text("Muffin             $2.75\\n")
p.text("Orange Juice       $4.00\\n")
p.text("--------------------------------\\n")

# Total
p.set(bold=True)
p.text("TOTAL:            $10.25\\n")
p.set(bold=False)
p.text("\\n")

# Footer
p.set(align='center')
p.text("Thank you!\\n")
p.text("Please come again\\n")
p.text("\\n")

# Cut paper
p.cut()
`;

