/**
 * Example 4: Combined Formatting
 * Demonstrates: Multiple formatting options applied simultaneously
 */

export const pythonCode = `from escpos.printer import Usb

# Initialize printer
p = Usb(0x04b8, 0x0e15)

# Bold and underlined
p.set(bold=True, underline=1)
p.text("Bold + Underline\\n")
p.set(bold=False, underline=0)

# Large, bold, and centered
p.set(align='center', bold=True, width=2, height=2)
p.text("BIG & BOLD\\n")
p.set(align='left', bold=False, width=1, height=1)

# Wide, underlined, right-aligned
p.set(align='right', underline=2, width=2)
p.text("Wide Underline\\n")
p.set(align='left', underline=0, width=1)

# Normal text with line feeds
p.text("\\n")
p.text("Mixed formatting allows\\n")
p.text("creative receipt designs\\n")
p.text("\\n")

# Cut paper
p.cut()
`;

