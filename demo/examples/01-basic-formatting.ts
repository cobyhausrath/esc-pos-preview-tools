/**
 * Example 1: Basic Text Formatting
 * Demonstrates: bold, underline, alignment
 */

export const pythonCode = `from escpos.printer import Usb

# Initialize printer
p = Usb(0x04b8, 0x0e15)

# Bold text
p.set(bold=True)
p.text("Bold Text\\n")
p.set(bold=False)

# Underlined text
p.set(underline=1)
p.text("Underlined Text\\n")
p.set(underline=0)

# Centered text
p.set(align='center')
p.text("Centered Text\\n")
p.set(align='left')

# Right aligned text
p.set(align='right')
p.text("Right Aligned\\n")
p.set(align='left')

# Normal text
p.text("Normal text\\n")

# Cut paper
p.cut()
`;

export const description = `
This example demonstrates basic text formatting capabilities:
- Bold text using set(bold=True)
- Underlined text using set(underline=1)
- Text alignment (center and right)
- Paper cutting
`;

